function gerarSenha(options) {
  if (options === void 0) {
    options = {};
  }
  if (!options.size) {
    options.size = 8;
  }
  var pass = [
    randomLetter().toLowerCase(),
    randomLetter().toUpperCase(),
    randomNumber(0, 9),
    randArray(["!", "@", "#", "$", "%", "^", "&", "*"]),
  ];
  var i = 4;
  for (i = 4; i <= options.size; i++) {
    var newchar = randArray([
      randomLetter().toLowerCase(),
      randomLetter().toUpperCase(),
      randomNumber(0, 9),
      randArray(["!", "@", "#", "$", "%", "^", "&", "*"]),
    ]);
    pass.push(newchar);
  }
  return pass.join("");
}

function slugify(value) {
  return value
    .toString()
    .toLowerCase()
    .replace(/[]+/g, "a") // Special Characters #1
    .replace(/[]+/g, "e") // Special Characters #2
    .replace(/[]+/g, "i") // Special Characters #3
    .replace(/[]+/g, "o") // Special Characters #4
    .replace(/[]+/g, "u") // Special Characters #5
    .replace(/[]+/g, "y") // Special Characters #6
    .replace(/[]+/g, "n") // Special Characters #7
    .replace(/[]+/g, "c") // Special Characters #8
    .replace(/[]+/g, "ss") // Special Characters #9
    .replace(/[]+/g, "ae") // Special Characters #10
    .replace(/[]+/g, "oe") // Special Characters #11
    .replace(/[%]+/g, "pct") // Special Characters #12
    .replace(/\s+/g, "-") // Replace spaces with -
    .replace(/[^\w\-]+/g, "") // Remove all non-word chars
    .replace(/\-\-+/g, "-") // Replace multiple - with single -
    .replace(/^-+/, "") // Trim - from start of text
    .replace(/-+$/, ""); // Trim - from end of text
}

function gerarSite(options) {
  if (options === void 0) {
    options = {};
  }
  var nome = randArray(EMPRESAS_TIPOS) + " " + randArray(EMPRESAS_NOMES);
  var dominio = ".com.br";
  var url = randArray(["http://", "https://"]);
  if (options.nome) {
    nome = options.nome;
  }
  if (options.dominio) {
    dominio = options.dominio;
  }
  if (options.url !== undefined) {
    url = options.url;
  }
  nome = slugify(nome);
  return url + nome + dominio;
}

